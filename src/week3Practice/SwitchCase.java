package week3Practice;

public class SwitchCase {

	public static void main(String[] args) {
		String day = "fri";
		switch (day) {
		case "sat":
			System.out.println("I am happy");
			break;
		case "sun":
			System.out.println("I am very happy");
			break;
		case "mon":
			System.out.println("I like");
			break;
		default:
			System.out.println("I hat");
		}
		}
		
}
		
